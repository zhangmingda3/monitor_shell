#!/bin/bash
#######方格脚本####
for i in `seq 1 8`	################################################################################################
do			###############################################################################################	
	for j in `seq 1 8`	###定义变量取值范围
	do	
	if [ $(($i%2)) -eq 1 ];then	##如果i的值是奇数，定义奇数行输出结果，第一次取值为1测试成功
		if [ $((j%2)) -eq 1 ];then	##当j的值是奇数时
		 echo -en "\033[41;37m  \033[0m" ##输出一种颜色
		else				##当j的值是偶数时，输出一种颜色
		 echo -en "\033[42;37m  \033[0m" ###当i取值为奇数时做这里的循环，为偶数时跳过
		 fi	#############################################################################################
	else	##当i为偶数时
		if [ $((j%2)) -eq 1 ];then
		 echo -en "\033[42;37m  \033[0m"
		else
		 echo -en "\033[41;37m  \033[0m"  ##当i的取值为偶数时做这里的循环
		 fi	####################################################################################
	 fi
	
	 done
	 echo	####当i取完一次值时做了里面的一个小循环形成一行，需要换行############
done	#################################################################################################################
